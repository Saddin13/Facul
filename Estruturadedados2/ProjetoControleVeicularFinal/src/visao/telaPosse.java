    private void jButtonAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAlterarActionPerformed
        // TODO add your handling code here:                                             
    try {
        int identificador = Integer.parseInt(jTextFieldId.getText());
        String placa = jTextFieldPlaca.getText();
        String cpf = jTextFieldCPF.getText();

        // Get current date for data_venda
        java.util.Date currentDate = new java.util.Date();
        java.sql.Date dataVenda = new java.sql.Date(currentDate.getTime());

        // Update existing record with data_venda
        veiculoProprietarioBD.setDataVenda(identificador, dataVenda);

        // Create new ownership record if new CPF provided
        if(!cpf.isEmpty()) {
            Posse novaPosse = new Posse(
                0, // new ID will be generated
                placa,
                getClienteIdFromCPF(cpf),
                dataVenda,
                null
            );
            veiculoProprietarioBD.incluir(novaPosse);
        }

        limparTela();
        mostrarVeiculosProprietariosNaGrid();
        
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(rootPane, "Alterar Visao: "+erro.getMessage());
    }
    }//GEN-LAST:event_jButtonAlterarActionPerformed

