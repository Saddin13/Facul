    private void BuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BuscarActionPerformed
        try {
        TelaBuscar telabuscar = new TelaBuscar(this,true);
        telabuscar.setLocationRelativeTo(null);
        telabuscar.setVisible(true);
        Aluno novo = new Aluno();
        String[] infBusca = new String[2];
        List<Aluno> resultadoBusca = null;
        infBusca = telabuscar.exportarDadosBusca(infBusca);
        if(infBusca != null){
        resultadoBusca = alunos.buscar(infBusca[0], infBusca[1]);
        telabuscar.receberResultadoBusca(resultadoBusca);
        telabuscar.dispose();
        }
        } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage() + "Incluir buscar\n");
        }
    }//GEN-LAST:event_BuscarActionPerformed

    private void IncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IncluirActionPerformed
       try {
        Aluno novo = new Aluno();
        TelaIncluir telaincluir = new TelaIncluir(this,true);
        telaincluir.setLocationRelativeTo(null);
        telaincluir.setVisible(true);
        novo = telaincluir.exportarDadosCadastro(novo);
        if(novo != null){
        alunos.incluir(novo);
        }
        Buscar.setEnabled(true);
        Incluir.setEnabled(true);
        Listar.setEnabled(true);
        Ordenar.setEnabled(true);
        } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage() + "Incluir Dados\n");
        }
    }//GEN-LAST:event_IncluirActionPerformed

    private void CarregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CarregarActionPerformed
        try { 
        JFileChooser fc = new JFileChooser();
        FileNameExtensionFilter filtro = new FileNameExtensionFilter("text","csv");
        fc.setFileFilter(filtro);
        int res = fc.showOpenDialog(null);
        if (res == JFileChooser.APPROVE_OPTION) {
            File arquivo = fc.getSelectedFile();
            DadosArqui dados = new DadosArqui(arquivo);
            alunos = dados.obterAlunos();
            
            Incluir.setEnabled(true);
            Buscar.setEnabled(true);
            Listar.setEnabled(true);
            Ordenar.setEnabled(true);
            
            ListarActionPerformed(evt);
        }
        } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage() + "Carregar Dados\n");
        } 
    }//GEN-LAST:event_CarregarActionPerformed

