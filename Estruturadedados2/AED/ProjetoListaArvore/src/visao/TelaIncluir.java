    private void jButton_incluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_incluirActionPerformed
        // TODO add your handling code here:
        try {
            //verificacao de vazio
            if(formattedTextFieldMatricula.getText().isEmpty()) throw  new Exception("Matricula nao pode estar vazia\n");
            if(jTextField_periodo.getText().isEmpty()) throw  new Exception("periodo nao pode estar vazia\n");
            if(jTextField_curso.getText().isEmpty()) throw  new Exception("curso nao pode estar vazia\n");
            if(jTextField_nome.getText().isEmpty()) throw  new Exception("nome nao pode estar vazia\n");
            if(jTextField_enfase.getText().isEmpty()) throw  new Exception("enfase nao pode estar vazia\n");
            
            //outras verificacoes
            if(!jTextField_periodo.getText().matches("^[0-9]+$"))throw  new Exception ("o camp periodo somente aceita numeros\n");
            if(formattedTextFieldMatricula.getText().length() != 15)throw new Exception("formato de inserção de matricula errado, ");
            
            //formatarMatricula(jTextField_matricula.getText());
            verificador = true;
            this.dispose();
        } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage() + "Tela Cadastro\n");
        }
    }//GEN-LAST:event_jButton_incluirActionPerformed

