    private void aplicarMascaraMatricula() {
    try {
        MaskFormatter formatter = new MaskFormatter("####.#.###.####");
        formatter.setPlaceholderCharacter('_');
        formatter.install(formattedTextFieldMatricula);
        formattedTextFieldMatricula.addFocusListener(new java.awt.event.FocusAdapter() {
            @Override
            public void focusGained(java.awt.event.FocusEvent evt) {
                formattedTextFieldMatricula.setCaretPosition(0); 
            }
        });
        
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao aplicar a máscara na matrícula: " + e.getMessage());
    }
}
    
    public Aluno exportarDadosCadastro (Aluno alunoNov) throws Exception{
         try {
       if(verificador == true){
        alunoNov = new Aluno(formattedTextFieldMatricula.getText().toUpperCase(), jTextField_nome.getText().toUpperCase(), jComboBox_turno.getSelectedItem().toString(), Integer.parseInt(jTextField_periodo.getText()), jTextField_enfase.getText().toUpperCase(), jTextField_curso.getText().toUpperCase());
       }
       else{
           alunoNov = null;
       }
       
        } catch (Exception erro) {
        JOptionPane.showMessageDialog(this, erro.getMessage() + "exportar Dados cadastros\n");
        }
            return alunoNov;
    }
    
    
