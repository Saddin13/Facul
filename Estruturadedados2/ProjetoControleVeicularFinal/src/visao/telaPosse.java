  private void jButtonIncluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonIncluirActionPerformed
    // TODO add your handling code here:
    try {
        int identificador = 0;
        String placa = jTextFieldPlaca.getText();
        String cpf = jTextFieldCPF.getText();
        
        // Get current date for data_aquisicao
        java.util.Date currentDate = new java.util.Date();
        java.sql.Date dataAquisicao = new java.sql.Date(currentDate.getTime());

        // First check if this vehicle already has an active ownership
        if(veiculoProprietarioBD.hasActiveOwnership(placa)) {
            // Close previous ownership by setting data_venda
            veiculoProprietarioBD.closeCurrentOwnership(placa, dataAquisicao);
        }

        // Create new ownership record
        Posse objPosse = new Posse(
            identificador,
            placa,
            getClienteIdFromCPF(cpf), // You'll need to implement this method
            dataAquisicao,
            null // data_venda starts as null
        );

        veiculoProprietarioBD.incluir(objPosse);
        limparTela();
        mostrarVeiculosProprietariosNaGrid();
        
    } catch (Exception erro) {
        JOptionPane.showMessageDialog(rootPane, "Incluir Visao: "+erro.getMessage());
    }
}

  
