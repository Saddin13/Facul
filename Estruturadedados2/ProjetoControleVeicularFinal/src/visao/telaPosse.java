    private void mostrarVeiculosProprietariosNaGrid() {
        try {
            ArrayList<Posse> listaDePosses = veiculoProprietarioBD.obterListaDePosses();
            DefaultTableModel model = (DefaultTableModel) jTableSaida.getModel();
            model.setNumRows(0);

            for(Posse posse : listaDePosses) {
                String[] linha = new String[5]; // Added columns for dates
                linha[0] = String.valueOf(posse.getIdentificador());
                linha[1] = String.valueOf(posse.getCPF());
                linha[2] = posse.getPlacaVeiculo();
                linha[3] = posse.getDataAquisicao().toString();
                linha[4] = posse.getDataVenda() != null ? posse.getDataVenda().toString() : "Ativo";
                model.addRow(linha);
            }
        } catch (Exception erro) {
            JOptionPane.showMessageDialog(rootPane, erro.getMessage());
        }
    }

    private int getClienteIdFromCPF(String cpf) throws Exception {
        return veiculoProprietarioBD.getClienteIdFromCPF(cpf);
    }
